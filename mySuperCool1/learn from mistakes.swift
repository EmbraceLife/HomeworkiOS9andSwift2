my lesson on the first app:

1. aspect fit and aspect fill are very different: fit or fill the square of image box
2. when working on a project, run simulator at every step to make sure you won't mistake too far
3. after you run simulation, cmd 1, 2, 3 to change the size of simulator
4. let auto-layout to do the complex work, and try to make layout simple and easy
5. UIImage select hidden option, means .hidden default value is true
6. in codes, once click button set background.hidden = false, meaning background appear
